
public class JavaBin  implements java.io.Serializable {
	
	private String student;
	private int id;
	
	public JavaBin() {
		
	}
	
	public String getStudent() {
		return student;
	}
	
	public void setStudent(String student) 
	{
		this.student = student;
	}
	
	
	public int getID() {
		return id;
	}
	
	public void setID(int id) 
	{
		this.id = id;
	}
}
